export type UiDict = (typeof dict)[UiLang];

// ── Context ────────────────────────────────────────────────────────────

interface LocaleCtx {
  lang: UiLang;
  setLang: (l: UiLang) => void;
  t: UiDict;
}

const KEY = "ui_lang";
const Ctx = createContext<LocaleCtx | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<UiLang>(() => {
    if (typeof window === "undefined") return "ru";
    const stored = localStorage.getItem(KEY) as UiLang | null;
    if (stored === "ru" || stored === "en" || stored === "es" || stored === "uk") return stored;
    return "ru";
  });

  function setLang(l: UiLang) {
    setLangState(l);
    localStorage.setItem(KEY, l);
  }

  // sync html lang attr
  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const t = dict[lang] as UiDict;

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useLocale() {
  const ctx = useContext(Ctx);
  if (!ctx) throw new Error("useLocale must be used inside LocaleProvider");
  return ctx;
}

// helper: pick service name by language
export function pickServiceNameByLang(
  s: { name: string; name_ru: string; name_en: string; name_es: string },
  lang: UiLang,
): string {
  const byLang =
    lang === "ru" ? s.name_ru :
    lang === "es" ? s.name_es :
    s.name_en; // en and uk both fall back to English service name
  return byLang || s.name_en || s.name_ru || s.name_es || s.name;
}

